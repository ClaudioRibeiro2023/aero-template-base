/**
 * Adiciona entry ao modules.config.ts como enabled: false.
 * Retorna true se adicionou, false se ja existia.
 */
function addToModulesConfig(name) {
  const kebab = kebabCase(name)
  const configPath = path.join(WEB_ROOT, 'modules.config.ts')

  if (!fs.existsSync(configPath)) {
    console.log(`  ⚠️  modules.config.ts nao encontrado — pule manualmente`)
    return false
  }

  let content = fs.readFileSync(configPath, 'utf-8')

  // Verificar se ja existe
  if (content.includes(`'${kebab}'`) || content.includes(`"${kebab}"`)) {
    console.log(`  ⚠️  modules.config.ts ja contem '${kebab}'`)
    return false
  }

  // Inserir antes do fechamento do objeto (ultima })
  const lastBrace = content.lastIndexOf('}')
  if (lastBrace === -1) {
    console.log(`  ⚠️  modules.config.ts formato inesperado`)
    return false
  }

  // Encontrar a ultima entry real (antes do fechamento)
  const beforeBrace = content.slice(0, lastBrace).trimEnd()
  const needsComma = !beforeBrace.endsWith(',')

  const newEntry = `${needsComma ? ',' : ''}\n\n  // ── Gerado por scaffold-module ──────────────────────────────\n  '${kebab}': { enabled: false },`
  content = content.slice(0, lastBrace) + newEntry + '\n' + content.slice(lastBrace)

  fs.writeFileSync(configPath, content, 'utf-8')
  console.log(`  ✅ Adicionado '${kebab}' ao modules.config.ts (enabled: false)`)
  return true
}

