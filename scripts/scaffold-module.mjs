#!/usr/bin/env node
/**
 * CLI Scaffolding — Generate new modules/functions
 * Usage:
 *   node scripts/scaffold-module.mjs <module-name> [--functions func1,func2]
 *
 * Example:
 *   node scripts/scaffold-module.mjs financeiro --functions receitas,despesas,relatorios
 */

import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const WEB_ROOT = path.resolve(__dirname, '..', 'apps', 'web')

function kebabCase(str) {
  return str.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase()
}

function pascalCase(str) {
  return str
    .split(/[-_\s]+/)
    .map(w => w.charAt(0).toUpperCase() + w.slice(1))
    .join('')
}

function titleCase(str) {
  return str
    .split(/[-_\s]+/)
    .map(w => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ')
}

function createDir(dirPath) {
  if (!fs.existsSync(dirPath)) {
    fs.mkdirSync(dirPath, { recursive: true })
    console.log(`  📁 Created ${path.relative(process.cwd(), dirPath)}`)
  }
}

function writeFile(filePath, content) {
  if (fs.existsSync(filePath)) {
    console.log(`  ⚠️  Skipped (exists): ${path.relative(process.cwd(), filePath)}`)
    return
  }
  fs.writeFileSync(filePath, content, 'utf-8')
  console.log(`  ✅ Created ${path.relative(process.cwd(), filePath)}`)
}

