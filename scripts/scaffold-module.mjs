/**
 * Auto-registra import e manifest no config/modules/index.ts.
 * Adiciona import statement + entry no ALL_MANIFESTS array.
 */
function addToModulesIndex(name) {
  const kebab = kebabCase(name)
  const camel = kebabCase(name).replace(/-([a-z])/g, (_, c) => c.toUpperCase()) + 'Manifest'
  const indexPath = path.join(WEB_ROOT, 'config', 'modules', 'index.ts')

  if (!fs.existsSync(indexPath)) {
    console.log(`  ⚠️  config/modules/index.ts nao encontrado`)
    return false
  }

  let content = fs.readFileSync(indexPath, 'utf-8')

  // Check if already registered
  if (content.includes(`'${kebab}.manifest'`) || content.includes(`"${kebab}.manifest"`)) {
    console.log(`  ⚠️  index.ts ja contem import de '${kebab}.manifest'`)
    return false
  }

  // Add import statement before ALL_MANIFESTS declaration
  const allManifestsLine = 'const ALL_MANIFESTS: ModuleManifest[] = ['
  const importLine = `import ${camel} from './${kebab}.manifest'\n`

  const allIdx = content.indexOf(allManifestsLine)
  if (allIdx === -1) {
    console.log(`  ⚠️  ALL_MANIFESTS nao encontrado em index.ts`)
    return false
  }

  // Insert import before the empty line preceding ALL_MANIFESTS
  content = content.slice(0, allIdx) + importLine + content.slice(allIdx)

  // Add to ALL_MANIFESTS array (before the closing bracket)
  const closingBracket = content.indexOf(']', content.indexOf(allManifestsLine))
  if (closingBracket === -1) {
    console.log(`  ⚠️  Formato inesperado de ALL_MANIFESTS`)
    return false
  }

  const beforeClose = content.slice(0, closingBracket).trimEnd()
  const needsComma = !beforeClose.endsWith(',')
  const entry = `${needsComma ? ',' : ''}\n  ${camel},\n`
  content = content.slice(0, closingBracket) + entry + content.slice(closingBracket)

  fs.writeFileSync(indexPath, content, 'utf-8')
  console.log(`  ✅ Auto-registrado '${kebab}' em config/modules/index.ts`)
  return true
}

