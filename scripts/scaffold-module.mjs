function hookTemplate(name) {
  const pascal = pascalCase(name)
  const camel = pascal.charAt(0).toLowerCase() + pascal.slice(1)
  const svcName = kebabCase(name).replace(/-/g, '')
  return `/**
 * use${pascal} — TanStack Query hooks (queries + mutations)
 * Auto-generated by scaffold-module
 */
import { useQuery, useMutation, useQueryClient } from '@tanstack/react-query'
import { ${svcName}Service } from '@/services/${kebabCase(name)}'

const KEYS = {
  all: ['${camel}'] as const,
  list: (params?: Record<string, string>) => [...KEYS.all, 'list', params] as const,
  detail: (id: string | null) => [...KEYS.all, 'detail', id] as const,
}

export function use${pascal}List(params?: Record<string, string>) {
  return useQuery({
    queryKey: KEYS.list(params),
    queryFn: () => ${svcName}Service.list(params),
  })
}

export function use${pascal}ById(id: string | null) {
  return useQuery({
    queryKey: KEYS.detail(id),
    queryFn: () => ${svcName}Service.getById(id!),
    enabled: !!id,
  })
}

export function useCreate${pascal}() {
  const queryClient = useQueryClient()
  return useMutation({
    mutationFn: (payload: Record<string, unknown>) => ${svcName}Service.create(payload),
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: KEYS.all })
    },
  })
}

export function useUpdate${pascal}() {
  const queryClient = useQueryClient()
  return useMutation({
    mutationFn: ({ id, ...payload }: { id: string } & Record<string, unknown>) =>
      ${svcName}Service.update(id, payload),
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: KEYS.all })
    },
  })
}

export function useDelete${pascal}() {
  const queryClient = useQueryClient()
  return useMutation({
    mutationFn: (id: string) => ${svcName}Service.remove(id),
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: KEYS.all })
    },
  })
}
`
}

